void arrayElements(int[] elements) // создаем метод присваивающий рандомные значения элементам длинны массива. Создаем метод, вкладываем в него массив.
{
     int size = elements.Length; // создаем переменную определяющую размер массива. Определяем длинну массива.
     
     for (int i = 0; i < size; i++) // задаем цикл перечислыющий индексы размера массива
     {
        elements[i] = new Random().Next(1, 10); // и вкладываем в переменную текущего индекса рандомное значение элемент
     }

}
void arrayPrint(int[] elementsPrint) // создаем метов выводящий элементы текущего индекса на экран
{
    int length = elementsPrint.Length; // определяем длинну массива
     for (int i = 0; i < length; i++) // создаем цикл перечисляющий индекс
     {
        Console.WriteLine(elementsPrint[i]); // выводим элемент текущего индекса на экран
     }

}
