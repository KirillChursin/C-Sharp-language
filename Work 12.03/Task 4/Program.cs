{
    int count = numb.Length; // задаем переменную длинны массива обращаясь к методу arrayElements присваивающую значения элементам массива.
    int position = -1; // присваиваем переменной, которая определяет индекс искомого элемента, начальный индекс -1, что бы если искомый элемент отсутствует в массиве -1 означало, что искомого числа нет в списке.
    for (int i = 0; i < count; i++) // запускаем цикл перечисляющий поочередно индексы массива от 0 до его длины
    {
        if (numb[i] == find) // создаем ветку где проверяется является ли значение элемента текущего индекса равным искомому значению 
        {
            position =  i; // если да, то присваеваем переменной значение текущего индекса
            
        }
    }
    return position; // возвращаем переменную в качестве ответа на вопрос, какой индекс имеет элемент массива, который мы ищем в find
} 

